#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int	a;
	int	b;

	a = 56;
	b = 72;
	ft_swap(&a, &b);
	printf("%d\n%d", a, b);
}
