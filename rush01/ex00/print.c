#include <unistd.h>

void	print(char **tab)
{
	int x;
	int y;

	x = 1;
	while (x <= 9)
	{
		y = 0;
		while (y <= 8)
		{
			write(1, &tab[x][y], 1);
			if (y < 8)
				write(1, " ", 1);
			y++;
		}
		write(1, "\n", 1);
		x++;
	}
}
