int ft_addition(int v1, int v2)
{
    return v1 + v2;
}
int ft_soustraction(int v1, int v2)
{
    return v1 - v2;
}
int ft_multiplication(int v1, int v2)
{
    return v1 * v2;
}
int ft_division(int v1, int v2)
{
    return v1 / v2;
}
int ft_modulo(int v1, int v2)
{
    return v1 % v2;
}